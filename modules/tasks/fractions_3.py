# –î–∏–º–∞ –æ—á–µ–Ω—å –ª—é–±–∏—Ç –º–∞—Ç–µ–º–∞—Ç–∏–∫—É, –ø–æ—ç—Ç–æ–º—É –¥–æ–º–∞ –æ–Ω –¥–æ–ª–≥–æ —ç–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç–∏—Ä–æ–≤–∞–ª, –ø—Ä–∏–¥—É–º—ã–≤–∞—è –∏ —Ä–µ—à–∞—è —Ä–∞–∑–Ω—ã–µ –∑–∞–¥–∞—á–∫–∏ —Å –ø—Ä–∞–≤–∏–ª—å–Ω—ã–º–∏ –Ω–µ—Å–æ–∫—Ä–∞—Ç–∏–º—ã–º–∏ –¥—Ä–æ–±—è–º–∏. –û–¥–Ω—É –∏–∑ —ç—Ç–∏—Ö –∑–∞–¥–∞—á –î–∏–º–∞ –ø—Ä–µ–¥–ª–∞–≥–∞–µ—Ç —Ä–µ—à–∏—Ç—å –≤–∞–º —Å –ø–æ–º–æ—â—å—é –∫–æ–º–ø—å—é—Ç–µ—Ä–∞.

# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç –Ω–∞–∏–±–æ–ª—å—à—É—é –ø—Ä–∞–≤–∏–ª—å–Ω—É—é –Ω–µ—Å–æ–∫—Ä–∞—Ç–∏–º—É—é –¥—Ä–æ–±—å —Å —Å—É–º–º–æ–π —á–∏—Å–ª–∏—Ç–µ–ª—è –∏ –∑–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—è —Ä–∞–≤–Ω–æ–π n.

# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ nn.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –æ—Ç–≤–µ—Ç –Ω–∞ –∑–∞–¥–∞—á—É.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –í–æ–∑–º–æ–∂–Ω–æ –≤–∞–º –ø–æ—Ç—Ä–µ–±—É–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è gcd(), –∫–æ—Ç–æ—Ä–∞—è –ø–æ–∑–≤–æ–ª—è–µ—Ç –Ω–∞—Ö–æ–¥–∏—Ç—å –Ω–∞–∏–±–æ–ª—å—à–∏–π –æ–±—â–∏–π –¥–µ–ª–∏—Ç–µ–ª—å (–ù–û–î) –¥–≤—É—Ö —á–∏—Å–µ–ª. –§—É–Ω–∫—Ü–∏—è —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞ –≤ –º–æ–¥—É–ª–µ math.
from fractions import Fraction as fr
n = 10 # –æ—Ç–≤–µ—Ç 3/7

# 1/9
lst=[]
for i in range(1,n):
    lst += [i/fr(n-i)] if (i<(n-i)) else ''

print(max(filter(lambda x: x.numerator + x.denominator == n, lst)))
